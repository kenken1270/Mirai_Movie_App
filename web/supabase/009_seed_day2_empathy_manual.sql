## 一人語りで成功させるポイント（制作メモ）
1. **視覚的フック（テロップ）** 単調になりやすいのでテロップを大きく・テンポよく。「宿題少なすぎ！」「将来が不安」「反抗期？」など心の声を画面端に散りばめると視聴維持率アップ。
2. **「妻」キーワード** 「私の妻も中国人なのですが、よくこう言っています……」で、外側の日本人ではなく中国人家庭の痛みを家庭内で共有する理解者であることを強調。
3. **中国語字幕（小紅書）** 意訳を意識。例：「日本の学校の優しさ」→「日本学校的“佛系”教育（悟り系教育）」など中国SNSで通じる表現へ。
4. **BGM** 前半は共感を呼ぶメロウなピアノ、後半は明るく前向きなリズムへ。

