1. **小道具** ファイルやタブレットを実物（またはイメージ図）で見せ、リアリティとシズル感を出す。
2. **中国語字幕のパワーワード** 自走（Zìzǒu）、PDCA、底层逻辑 など、信頼とトレンドを両立。
3. **AIの見せ方** 「教える」のではなく「振り返りを助ける・伴走」が主役は子ども、というブランドを守る。
4. **BGM・テンポ** 15〜60sは知的でテンポ良いインスト、60s〜は未来への期待が伝わる明るめ。

